///////////////////////////////////////////////////////////////////////
//  给定一个整数 (32 位有符号整数)，请编写一个函数来判断它是否是 4 的幂次方。
///////////////////////////////////////////////////////////////////////
//  https://leetcode-cn.com/problems/power-of-four/description/
///////////////////////////////////////////////////////////////////////

class Solution 
{
    public:
    
    bool isPowerOfFour(int num)
    {
        return num > 0 ? (log(num) / log(4)) - (int)(log(num) / log(4)) <= 0.000000001f ? true : false : false;
    }
};
