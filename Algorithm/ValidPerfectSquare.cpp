/////////////////////////////////////////////////////////////////////////////////////////
//  给定一个正整数 num，编写一个函数，如果 num 是一个完全平方数，则返回 True，否则返回 False。
//  说明：不要使用任何内置的库函数，如  sqrt。
/////////////////////////////////////////////////////////////////////////////////////////
//  https://leetcode-cn.com/problems/valid-perfect-square/description/
/////////////////////////////////////////////////////////////////////////////////////////

class Solution 
{
    public:
    
    bool isPerfectSquare(int num) 
    {
        for (int i = 1; num > 0; i += 2)
        {
            num -= i;
        }
        
        return num == 0;
    }
};
